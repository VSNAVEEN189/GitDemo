#Escape sequences are special characters that are used for some special insider.

#\n- new/next line
#\t- tab
#\\- backslash
#\'- inserts a single quote inside a single-quoted string

#\n- Gives new line or next line
print("Hello everyone. \nHow are you?")

#\t- To give some spaces in string
print("John\t20")

#\\- to insert single backslash
print("new\\old")

#\'- To insert single quote in single quoted string as well with double quoted string
print('This is Python\'s class')

